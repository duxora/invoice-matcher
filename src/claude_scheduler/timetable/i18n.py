DEFAULT_LANG = "vi"


def get_translator(lang: str = "") -> Callable[[str], str]:
    """Return a translation function for the given language."""
    lang = lang or DEFAULT_LANG
    strings = TRANSLATIONS.get(lang, TRANSLATIONS["en"])

    def t(key: str) -> str:
        return strings.get(key, TRANSLATIONS["en"].get(key, key))

    return t
